
class vehiculo (object):
    patente = None
    cant_ruedas = None
    anio_fabricacion = None

    def set_patente (self , patente):
        self.patente = patente

    def set_cant_ruedas (self , cant_ruedas):
        self.cant_ruedas = cant_ruedas

    def set_anio_fabricacion (self , anio_fabricacion):
        self.anio_fabricacion = anio_fabricacion

